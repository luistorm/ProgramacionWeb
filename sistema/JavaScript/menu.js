function menu_cli() {
	var vec = document.getElementsByClassName("cli")
	for (var i = 0; i < vec.length; i++) {
		vec[i].style.display = "list-item"
	}
	vec = document.getElementsByClassName("pro")
	for (var i = 0; i < vec.length; i++) {
		vec[i].style.display = "none"
	}
	vec = document.getElementsByClassName("fac")
	for (var i = 0; i < vec.length; i++) {
		vec[i].style.display = "none"
	}
	vec = document.getElementsByClassName("for")
	for (var i = 0; i < vec.length; i++) {
		vec[i].style.display = "none"
	}
	vec = document.getElementsByClassName("repo")
	for (var i = 0; i < vec.length; i++) {
		vec[i].style.display = "none"
	}
}

